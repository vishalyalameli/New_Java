class SwitchDemo6{
	public static void main(String [] args){
		double  x = 25.5;
		switch (x){
			case 25.5 : System.out.println("5.5");
				    break;
			case 15.5 : System.out.println("5.0");
						    break;
			case  16 :  System.out.println(" 45.01");
			default : System.out.println("Invalid value");
		}
	}
}
