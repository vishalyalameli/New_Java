class DayWeek{
	public static void main(String [] args){
                  int daynumber = 10;
		if( daynumber == 1 ){
			System.out.println(daynumber + " : Monday");
		}else if(daynumber == 2){
			System.out.println("Tuesday");
		}else if(daynumber == 3){
			System.out.println("Wed");
		}else if(daynumber == 4){
			System.out.println("Thu");
		}else if( daynumber == 5){
			System.out.println("Fir");
		}else if(daynumber == 6){
			System.out.println("Sat");
		}else if(daynumber == 7){
			System.out.println("sun");
		}else{
			System.out.println(" day number must between  1 -7 ");
		}
	}
}
