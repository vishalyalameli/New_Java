class SwitchDemo6{
	public static void main(String [] args){
		double x = 25.5;

		switch((int )x){
		
		case 5.5: System.out.println("5.5");
			  break;
		case 15.5 : System.out.println("15.5");
			    break;
		case 35.5: System.out.println("35.5");
			   break;

		default: System.out.println( "No match");
			 break;

	}
	}


}

// float is not allowed for the switch cases


			

