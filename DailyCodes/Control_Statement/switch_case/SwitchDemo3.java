class SwitchDemo3{
	public static void main(String [] args){
		char ch = 'E';
		switch(ch){
		             default : System.out.println("Invalid Character");
				       break;
			case 'A' :System.out.println("A");
				  break;
		         case 'B' : System.out.println("B");
				    break;
		         case 'C' : System.out.println("c");
			case 'D' : System.out.println("D");
				   
		}
	}
}
