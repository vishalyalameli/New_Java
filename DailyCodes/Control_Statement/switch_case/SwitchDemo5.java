class SwitchDemo5{
	public static void main(String [] args){
		int  x = 65;

		switch(x){
			case 65 : System.out.println("65");
				  break;
		         case 66 : System.out.println("66");
				   break;


		          case  'B' : System.out.println("B");
			  default : System.out.println("Not Match");
		}
	}
}
